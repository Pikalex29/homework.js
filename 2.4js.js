/**
 * Функция сложения параметров
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */

function addition(a, b) {
    return a + b
}

/** Вычетание
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function subtraction(a, b) {
    return a - b
}

/** Деление
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function division(a, b) {
    return a / b
}

/** Умножение
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function multiplication(a, b) {
    return a * b
}